		this.tombolulang.alpha = 0.3;
		this.tombolulang.mouseEnabled = false;
		
		this.elektron.stop();
		this.elektron.visible = false;
		
		this.panah.stop();
		this.panah.visible = false;
		
		this.nilaimg.visible = false;
		this.nilaimn.visible = false;
		this.nilaizn.visible = false;
		this.nilaipb.visible = false;
		
		// Drag and drop setup
		var obyeks = [this.mg, this.mn, this.zn, this.pb];
		var boxs = [this.kotak];
		let that = this;
	
		obyeks.forEach((obj, idx) => {
			obj.addEventListener("mousedown", function(e) {
				// Store initial position
				if (!e.currentTarget.initX) {
					e.currentTarget.initX = e.currentTarget.x;
					e.currentTarget.initY = e.currentTarget.y;
				}
				
				let offset = {
					x: e.currentTarget.x - e.stageX,
					y: e.currentTarget.y - e.stageY
				};
				
				e.currentTarget.on("pressmove", function(moveEvent) {
					moveEvent.currentTarget.x = moveEvent.stageX + offset.x;
					moveEvent.currentTarget.y = moveEvent.stageY + offset.y;
					that.stage.update();
				});
			});
	
			obj.addEventListener("pressup", function(e) {
				e.currentTarget.removeAllEventListeners("pressmove");
				
				// Check collision with box
				boxs.forEach(box => {
					if (Math.abs(e.currentTarget.x - box.x) < 50 && 
						Math.abs(e.currentTarget.y - box.y) < 50) {
						e.currentTarget.x = box.x;
						e.currentTarget.y = box.y;
						updateScore(idx);
					} else {
						// Return to initial position if not dropped on target
						e.currentTarget.x = e.currentTarget.initX;
						e.currentTarget.y = e.currentTarget.initY;
					}
				});
				
				that.stage.update();
			});
		});
	
		function updateScore(idx) {
			that.elektron.visible = true;
			that.elektron.gotoAndPlay(1);
			
			that.panah.visible = true;
			that.panah.play(); // Cukup play() aja, gak perlu pake stop
			// Hapus event listener complete karena kita mau loop
			
			switch(idx) {
				case 0: that.nilaimg.visible = true; break;
				case 1: that.nilaimn.visible = true; break;
				case 2: that.nilaizn.visible = true; break;
				case 3: that.nilaipb.visible = true; break;
			}
			
			obyeks[idx].mouseEnabled = false;
			that.tombolulang.alpha = 1;
			that.tombolulang.mouseEnabled = true;
		}
	
		function resetState() {
			// Reset visibility of UI elements
			that.elektron.visible = false;
			that.elektron.gotoAndStop(0);
			
			that.panah.visible = false;
			that.panah.gotoAndStop(0);
			
			// Reset nilai voltmeter
			that.nilaimg.visible = false;
			that.nilaimn.visible = false;
			that.nilaizn.visible = false;
			that.nilaipb.visible = false;
			
			// Find which elektroda was tested (the one at kotak position)
			const testedElektroda = obyeks.find(obj => 
				obj.x === that.kotak.x && obj.y === that.kotak.y
			);
			
			if (testedElektroda) {
				// Remove listeners only from tested elektroda
				testedElektroda.removeAllEventListeners("pressmove");
				testedElektroda.removeAllEventListeners("mousedown");
				
				// Reset position of tested elektroda only
				testedElektroda.x = testedElektroda.initX;
				testedElektroda.y = testedElektroda.initY;
				
				// Re-enable dragging for tested elektroda
				testedElektroda.mouseEnabled = true;
				
				// Reattach drag events only to tested elektroda
				testedElektroda.addEventListener("mousedown", function(e) {
					if (!e.currentTarget.initX) {
						e.currentTarget.initX = e.currentTarget.x;
						e.currentTarget.initY = e.currentTarget.y;
					}
					
					let offset = {
						x: e.currentTarget.x - e.stageX,
						y: e.currentTarget.y - e.stageY
					};
					
					e.currentTarget.on("pressmove", function(moveEvent) {
						moveEvent.currentTarget.x = moveEvent.stageX + offset.x;
						moveEvent.currentTarget.y = moveEvent.stageY + offset.y;
						that.stage.update();
					});
				});
			}
			
			// Reset button state
			that.tombolulang.alpha = 0.3;
			that.tombolulang.mouseEnabled = true;
			
			// Force stage update
			that.stage.update();
		}
	
